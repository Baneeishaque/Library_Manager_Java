    public void authentication() {
        if (unam.getText() == null || userid.getText() == null || comdep.getSelectedItem() == null || comsem.getSelectedItem() == null || uadd.getText() == null || dob.getDate() == null || phnum.getText() == null || adob.getDate() == null) {
            flag = 1;
            JOptionPane.showMessageDialog(null, "Fill all Fields");
        } else {
            if (phnum.getText().length() != 10 && phnum.getText().matches("[0-9]$*")) {
                flag = 2;
                JOptionPane.showMessageDialog(null, "Phone Number should be 10 digits");
            } else {
                flag = 0;
            }
        }
    }

