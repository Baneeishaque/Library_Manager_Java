    private void useridActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_useridActionPerformed
        try {
            String str = "select * from user where usrid=?";
            pst = con.prepareStatement(str);
            pst.setString(1, userid.getText());
            rs = pst.executeQuery();
            if (rs.absolute(1)) {
                rs = pst.executeQuery();
                if (rs.next()) {
                    String na = rs.getString("Name");
                    unam.setText(na);
                    String ad = rs.getString("Address");
                    uadd.setText(ad);
                    String de = rs.getString("Department");
                    comdep.setSelectedItem(de);
                    String s = rs.getString("Sem");
                    comsem.setSelectedItem(s);
                    String p = rs.getString("Phonnum");
                    phnum.setText(p);
                    java.sql.Date do1 = rs.getDate("Dob");
                    java.util.Date do2 = new java.util.Date(do1.getTime());
                    dob.setDate(do2);
                    java.sql.Date ad1 = rs.getDate("AdmsnDate");
                    java.util.Date ad2 = new java.util.Date(ad1.getTime());
                    adob.setDate(ad2);
                    gen = rs.getString("Gender");
                    if (gen.equals("male")) {
                        rma.setSelected(true);
                    } else {
                        rfe.setSelected(true);
                    }
                    passcode.setText(rs.getString("password"));
                    pst.execute();
                }
            } else {
                clearother();

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_useridActionPerformed

